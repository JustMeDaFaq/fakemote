static inline u8 ror8(u8 word, unsigned int shift)
{
    shift %= 8;
    return (word >> shift) | (word << (8 - shift));
}

static void generate_key_data(u8 key[static 6], const u8 rand[static 10], u32 idx)
{
    static const u8 ans_tbl[7][6] = {
        { 0xa8, 0x77, 0xa6, 0xe0, 0xf7, 0x43 },
        { 0x5a, 0x35, 0x85, 0xe2, 0x72, 0x97 },
        { 0x8f, 0xb7, 0x1a, 0x62, 0x87, 0x38 },
        { 0x0d, 0x67, 0xc7, 0xbe, 0x4f, 0x3e },
        { 0x20, 0x76, 0x37, 0x8f, 0x68, 0xb7 },
        { 0xa9, 0x26, 0x3f, 0x2b, 0x10, 0xe3 },
        { 0x30, 0x7e, 0x90, 0x0e, 0x85, 0x0a },
    };
    u8 t0[10];

    for (int i = 0; i < sizeof(t0); i++)
        t0[i] = keygen_sbox_1st_party[rand[i]];

    key[0] = (ror8(ans_tbl[idx][0] ^ t0[5], t0[2]) - t0[9]) ^ t0[4];
    key[1] = (ror8(ans_tbl[idx][1] ^ t0[1], t0[0]) - t0[5]) ^ t0[7];
    key[2] = (ror8(ans_tbl[idx][2] ^ t0[6], t0[8]) - t0[2]) ^ t0[0];
    key[3] = (ror8(ans_tbl[idx][3] ^ t0[4], t0[7]) - t0[3]) ^ t0[2];
    key[4] = (ror8(ans_tbl[idx][4] ^ t0[1], t0[6]) - t0[3]) ^ t0[4];
    key[5] = (ror8(ans_tbl[idx][5] ^ t0[7], t0[8]) - t0[5]) ^ t0[9];
}

static void generate_tables(u8 ft[static 8], u8 sb[static 8], u8 rand[static 10], u8 key[static 6],
                            const sbox_t sbox_a, const sbox_t sbox_b)
{
    ft[0] = sbox_a[key[4]] ^ sbox_b[rand[3]];
    ft[1] = sbox_a[key[2]] ^ sbox_b[rand[5]];
    ft[2] = sbox_a[key[5]] ^ sbox_b[rand[7]];
    ft[3] = sbox_a[key[0]] ^ sbox_b[rand[2]];
    ft[4] = sbox_a[key[1]] ^ sbox_b[rand[4]];
    ft[5] = sbox_a[key[3]] ^ sbox_b[rand[9]];
    ft[6] = sbox_a[rand[0]] ^ sbox_b[rand[6]];
    ft[7] = sbox_a[rand[1]] ^ sbox_b[rand[8]];

    sb[0] = sbox_a[key[0]] ^ sbox_b[rand[1]];
    sb[1] = sbox_a[key[5]] ^ sbox_b[rand[4]];
    sb[2] = sbox_a[key[3]] ^ sbox_b[rand[0]];
    sb[3] = sbox_a[key[2]] ^ sbox_b[rand[9]];
    sb[4] = sbox_a[key[4]] ^ sbox_b[rand[7]];
    sb[5] = sbox_a[key[1]] ^ sbox_b[rand[8]];
    sb[6] = sbox_a[rand[3]] ^ sbox_b[rand[5]];
    sb[7] = sbox_a[rand[2]] ^ sbox_b[rand[6]];
}

void wiimote_crypto_generate_key_from_extension_key_data(struct wiimote_encryption_key_t *ext_key,
                                                         const u8 key_data[static 16])
{
    u8 rand[10];
    u8 key[6], check_key[6];
    u32 idx;

    reverse_memcpy(rand, key_data, sizeof(rand));
    reverse_memcpy(key, key_data + sizeof(rand), sizeof(key));

    /* Bruteforcing */
    for (idx = 0; idx != 7; ++idx) {
        generate_key_data(check_key, rand, idx);
        if (memcmp(key, check_key, sizeof(key)) == 0)
            break;
    }

    generate_tables(ext_key->ft, ext_key->sb, rand, key, sboxes_1st_party[idx],
                    sboxes_1st_party[idx + 1]);
}

void wiimote_crypto_encrypt(u8 *data, const struct wiimote_encryption_key_t *key, u32 addr,
                            u32 size)
{
    for (u32 i = 0; i < size; ++i, ++addr)
        data[i] = (data[i] - key->ft[addr % 8]) ^ key->sb[addr % 8];
}
